def p(pid, domain, desc, difficulty, optimal, acceptable=None, bad=None, reasoning=""):
    return {
        "problem_id": pid,
        "domain": domain,
        "description": desc,
        "difficulty": difficulty,
        "reference_answer": {
            "optimal_strategies": optimal,
            "acceptable_strategies": acceptable or [],
            "explicitly_bad_strategies": bad or [],
            "reasoning": reasoning,
        },
        "coverage_tags": optimal + (acceptable or []),
    }


